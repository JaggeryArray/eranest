    """ANSI color codes for terminal output."""
    RESET = "\033[0m"
    RED = "\033[0;31m"
    GREEN = "\033[0;32m"
    YELLOW = "\033[0;33m"
    BLUE = "\033[0;34m"
    PURPLE = "\033[0;35m"
    CYAN = "\033[0;36m"
    WHITE = "\033[0;37m"
    GREEN_BRIGHT = "\033[0;92m"
    RED_BRIGHT = "\033[0;91m"
    YELLOW_BRIGHT = "\033[0;93m"
    BLUE_BRIGHT = "\033[0;94m"
    CYAN_BRIGHT = "\033[0;96m"